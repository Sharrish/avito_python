def ask_question(q_id):
    question_prompt = {
        'type': 'list',
        'name': 'answer',
        'message': questions_tree[q_id]['question'],
        'choices': [q['answer'] for q in questions_tree[q_id]['answers']]
    }
    answer = prompt(question_prompt)
    return answer['answer']


def get_next_question_id(q_id, answer):
    for ans in questions_tree[q_id]['answers']:
        if ans['answer'] == answer:
            next_q_id = ans['next_q_id']
            break
    return next_q_id


